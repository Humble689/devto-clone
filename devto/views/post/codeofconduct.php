    <h1>Code of Conduct</h1>
    <p><strong>Last updated July 31, 2023</strong></p>

    <p>
        All participants of DEV Community are expected to abide by our 
        <a href="#">Code of Conduct</a> and 
        <a href="#">Terms of Service</a>, both online and during in-person 
        events that are hosted and/or associated with DEV Community.
    </p>


    <h2>Our Pledge</h2>

    <p>
        In the interest of fostering an open and welcoming environment, we as 
        moderators of DEV Community pledge to make participation in our project 
        and our community a harassment-free experience for everyone, regardless 
        of age, body size, disability, ethnicity, gender identity and expression, 
        level of experience, nationality, personal appearance, race, religion, 
        or sexual identity and orientation.
    </p>


    <h2>Our Standards</h2>

    <p>
        Examples of behavior that contributes to creating a positive environment include:
    </p>

    <ul>
        <li>Using welcoming and inclusive language</li>
        <li>Being respectful of differing viewpoints and experiences</li>
        <li>
            Referring to people by their pronouns and using gender-neutral 
            pronouns when uncertain
        </li>
        <li>Gracefully accepting constructive criticism</li>
        <li>Focusing on what is best for the community</li>
        <li>Showing empathy towards other community members</li>
        <li>
            Citing sources if used to create content (for guidance see 
            <a href="#">DEV Community: How to Avoid Plagiarism</a>)
        </li>
        <li>
            Following our AI Guidelines and disclosing AI assistance if used 
            to create content
        </li>
    </ul>


    <p>
        Examples of unacceptable behavior by participants include:
    </p>

    <ul>
        <li>
            The use of sexualized language or imagery and unwelcome sexual 
            attention or advances
        </li>

        <li>
            The use of hate speech or communication that is racist, homophobic, 
            transphobic, ableist, sexist, or otherwise prejudiced/discriminatory 
            (i.e. misusing or disrespecting pronouns)
        </li>

        <li>
            Trolling, insulting/derogatory comments, and personal or political attacks
        </li>

        <li>Public or private harassment</li>

        <li>
            Publishing others' private information, such as a physical or electronic 
            address, without explicit permission
        </li>

        <li>Plagiarizing content or misappropriating works</li>

        <li>
            Other conduct which could reasonably be considered inappropriate 
            in a professional setting
        </li>

        <li>Dismissing or attacking inclusion-oriented requests</li>
    </ul>


    <h2>Community Values</h2>

    <p>
        We pledge to prioritize marginalized people's safety over privileged 
        people's comfort. We will not act on complaints regarding:
    </p>

    <ul>
        <li>
            "Reverse" -isms, including "reverse racism," "reverse sexism," 
            and "cisphobia"
        </li>

        <li>
            Reasonable communication of boundaries, such as 
            "leave me alone," "go away," or "I'm not discussing this with you."
        </li>

        <li>
            Someone's refusal to explain or debate social justice concepts
        </li>

        <li>
            Criticisms of racist, sexist, cissexist, or otherwise oppressive 
            behavior or assumptions
        </li>
    </ul>


    <h2>Enforcement</h2>

    <p>
        Violations of the Code of Conduct may be reported by contacting the team 
        via the abuse report form or by sending an email to 
        <a href="mailto:support@dev.to">support@dev.to</a>.
    </p>

    <p>
        All reports will be reviewed and investigated and will result in a 
        response that is deemed necessary and appropriate to the circumstances. 
        Further details of specific enforcement policies may be posted separately.
    </p>

    <p>
        Moderators have the right and responsibility to remove comments or other 
        contributions that are not aligned to this Code of Conduct or to suspend 
        temporarily or permanently any members for other behaviors that they deem 
        inappropriate, threatening, offensive, or harmful.
    </p>

    <p>
        If you agree with our values and would like to help us enforce the Code 
        of Conduct, you might consider volunteering as a DEV moderator. Please 
        check out the DEV Community Moderation page for information about our 
        moderator roles and how to become a mod.
    </p>


